import java.lang.*;

/*
 * Convention: 관습, 관례
 * GNU Style
 * GSD Style
 * K&R Style
 */

public class Exam_01 {
	public static void main(String[] args) {
		System.out.println("Hello World");
		System.out.println("JAVA Study! 한글도 되나?");
		
		/*
		 * 문자열 >> ""
		 * 한글자 >> ''
		 * 숫자 >> No Quote (따음표 사용 안함)
		 */
		System.out.println(10);
		System.out.println(3.14);
		System.out.println('A');
		
		// Ctrl + Space << 코드 어시스턴스
		System.out.println();
	}
}